public class HSVColor {

    private float h;
    private float s;
    private float v;

    public HSVColor(float h, float s, float v){

        this.h = h;
        this.s = s;
        this.v = v;


    }

    public float getH() {
        return h;
    }

    public float getS() {
        return s;
    }

    public float getV() {
        return v;
    }


}
